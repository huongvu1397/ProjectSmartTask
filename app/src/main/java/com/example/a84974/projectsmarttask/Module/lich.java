package com.example.a84974.projectsmarttask.Module;

public class lich {
    private String tenthe,tenlist,tenbang,phut,gio;

    public lich() {
    }

    public lich(String tenthe, String tenlist, String tenbang, String phut, String gio) {
        this.tenthe = tenthe;
        this.tenlist = tenlist;
        this.tenbang = tenbang;
        this.phut = phut;
        this.gio = gio;
    }

    public String getTenthe() {
        return tenthe;
    }

    public void setTenthe(String tenthe) {
        this.tenthe = tenthe;
    }

    public String getTenlist() {
        return tenlist;
    }

    public void setTenlist(String tenlist) {
        this.tenlist = tenlist;
    }

    public String getTenbang() {
        return tenbang;
    }

    public void setTenbang(String tenbang) {
        this.tenbang = tenbang;
    }

    public String getPhut() {
        return phut;
    }

    public void setPhut(String phut) {
        this.phut = phut;
    }

    public String getGio() {
        return gio;
    }

    public void setGio(String gio) {
        this.gio = gio;
    }
}
